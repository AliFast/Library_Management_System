    private void checkOutButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_checkOutButtonActionPerformed

        String cnicObj = null;
        try {

            cnicObj = cnicForCheck.getText();
            if (cnicObj.equals("")) {
                searchMessageLabel.setText("Please write CNIC of exactly 13 digits");
                searchMessageLabel.setVisible(true);
                return;

            }

            if (!CnicValidation.validate(cnicObj)) {
                searchMessageLabel.setText("Please write CNIC of exactly 13 digits");
                searchMessageLabel.setVisible(true);
                return;

            }

            if (ExistenseValidation.cnicValidateBorrower(cnicObj).equals("1")) {

                searchMessageLabel.setText("This CNIC does not exists in Database or is not related to a Borrower");
                searchMessageLabel.setVisible(true);
                return;

            }

            DefaultTableModel model = (DefaultTableModel) librariyanSearchBooksTable.getModel();
            int selectedRowIndex = librariyanSearchBooksTable.getSelectedRow();

            if (selectedRowIndex == -1) {
                searchMessageLabel.setText("Please Select a Row from Table");
                searchMessageLabel.setVisible(true);
                return;

            }

            String isbnObject = model.getValueAt(selectedRowIndex, 0).toString();

            BookQueries b1 = new BookQueries();
            Book bookList = b1.findByIsbn(isbnObject);
            LmsuserQueries b2 = new LmsuserQueries();

            Lmsuser userList = b2.findByCnic(cnicObj);
            String msg = BorrowedBooksValidation.validate(userList);

            if (!msg.equals("1")) {
                searchMessageLabel.setText("Borrower Already has 2 Borrowed Books");
                searchMessageLabel.setVisible(true);
                return;

            }

            msg = LoanValidation.validate(userList, bookList);
            if (!msg.equals("1") && !msg.equals("2")) {
                searchMessageLabel.setText(msg);
                searchMessageLabel.setVisible(true);
                return;

            }

            HoldBookFunctions hb = new HoldBookFunctions();

            //Book was on hold by requesting user. removing the on hold record
            if (msg.equals("1")) {
                hb.deleteHoldBook(userList.getId(), bookList.getId());

            }

            AddLoan l1 = new AddLoan();
            UpdateBook ub = new UpdateBook();
            
            if(l1.addLoan(userList, bookList) && ub.updateAvailaibility(bookList.getId(), false)) {
            	
            	searchMessageLabel.setText("Book Checked Out Successfully");
            }else {
            	throw new Exception();
            }
            

            

        } catch (Exception e) {

            searchMessageLabel.setText("Unable to Check Out");
            searchMessageLabel.setVisible(true);

        }


    }//GEN-LAST:event_checkOutButtonActionPerformed

    private void checkInButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_checkInButtonActionPerformed

        String cnicObj = null;
        try {

            cnicObj = cnicForCheck.getText();
            if (cnicObj.equals("")) {
                searchMessageLabel.setText("Please write CNIC of exactly 13 digits");
                searchMessageLabel.setVisible(true);
                return;

            }

            if (!CnicValidation.validate(cnicObj)) {
                searchMessageLabel.setText("Please write CNIC of exactly 13 digits");
                searchMessageLabel.setVisible(true);
                return;

            }

            if (ExistenseValidation.cnicValidateBorrower(cnicObj).equals("1")) {

                searchMessageLabel.setText("This CNIC does not exists in Database or is not related to a Borrower");
                searchMessageLabel.setVisible(true);
                return;

            }

            DefaultTableModel model = (DefaultTableModel) librariyanSearchBooksTable.getModel();
            int selectedRowIndex = librariyanSearchBooksTable.getSelectedRow();

            if (selectedRowIndex == -1) {
                searchMessageLabel.setText("Please select a row from table");
                searchMessageLabel.setVisible(true);
                return;

            }

            String isbnObject = model.getValueAt(selectedRowIndex, 0).toString();

            BookQueries b1 = new BookQueries();
            Book bookList = b1.findByIsbn(isbnObject);

            LoanQueries loanObject = new LoanQueries();
            List<Loan> loanListLocal = loanObject.findBookInLoan(bookList);

            // if the book is available it cannot be checked In
            if (bookList.getAvailability()) {
                searchMessageLabel.setText("Book was not checked out");
                searchMessageLabel.setVisible(true);
                return;

            } else {

                // book is available and it was never loaned to anyone condition
                if (loanListLocal.isEmpty()) {
                    searchMessageLabel.setText("Book is not available and was never Loaned by anyone");
                    searchMessageLabel.setVisible(true);
                    return;

                }

            }

            LmsuserQueries b2 = new LmsuserQueries();

            Lmsuser userList = b2.findByCnic(cnicObj);

            LoanQueries l1 = new LoanQueries();
            List<Loan> loanList = l1.findLoanList(userList, bookList);

            if (loanList.isEmpty()) {
                searchMessageLabel.setText("Book was not borrowed by this user");
                searchMessageLabel.setVisible(true);
                return;
            }

            UpdateLoan ul = new UpdateLoan();
            UpdateBook ub = new UpdateBook();
            ul.updateActualReturnDate(loanList.get(0).getLoanid());
            ub.updateAvailaibility(bookList.getId(), true);

            searchMessageLabel.setText("Book Checked in Successfully");
            searchMessageLabel.setVisible(true);

        } catch (Exception e) {
            searchMessageLabel.setText("Unable to Check In");
            searchMessageLabel.setVisible(true);

        }


    }//GEN-LAST:event_checkInButtonActionPerformed

